package flowcontrol;

public class Forloop {
	public static void main(String[] args) {
		  for(int i=0;i<=4 ;i++)
		  {
			  System.out.println(i);
		  }
		  int a=1;
		  for(System.out.println("hello");a<=100;a++)
		  {
			 System.out.print(a+"  ");
		  }
		  int b=100;
		  for(System.out.println("hello");b>=1;b--)
		  {
			 System.out.print(b+"  ");

		  }
		  
		  
	}
 

}
